package com.testvagrant.Subscription;

public class DailyRate {

	private double weekdaysRate;
	private double saturdayRate;
	private double sundayRate;
	
	public double getSundayRate() {
		return sundayRate;
	}
	public void setSundayRate(double sundayRate) {
		this.sundayRate = sundayRate;
	}
	public double getWeekdaysRate() {
		return weekdaysRate;
	}
	public void setWeekdaysRate(double weekdaysRate) {
		this.weekdaysRate = weekdaysRate;
	}
	public double getsaturdayRate() {
		return saturdayRate;
	}
	public void saturdayRate(double saturdayRate) {
		this.saturdayRate = saturdayRate;
	}
	
	
	
}
